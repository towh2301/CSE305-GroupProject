    public static void addToRoomStatus(HashMap<String, Integer> roomType, String roomName, int numOfStudent) {
        int tempStudent = roomType.get(roomName) + numOfStudent;
        roomType.put(roomName, tempStudent);
    }

    public static void chooseRoom(Room room){
        int current = room.getCurrent() + 1;
        room.setCurrent(current);
    }
    
