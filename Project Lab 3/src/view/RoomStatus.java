    public static void createObject(List<Room> room) {

        for (int i = 0; i < room.size(); i++) {
            //initialize variable for oobject
            String roomName = room.get(i).getName();
            int roomCurrent = room.get(i).getCurrent();
            int roomCapacity = room.get(i).getCapacity();
            
            //add infor to table
            Object[] object = new Object[]{roomName, roomCurrent, roomCapacity};
            addRowTable(object);
        }
    }

    public static void addRowTable(Object[] object) {
        DefaultTableModel model = (DefaultTableModel) RoomStatus.roomStatusTable.getModel();
        model.addRow(object);
    }

